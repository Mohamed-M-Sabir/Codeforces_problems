// Simple Calculator.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>
using namespace std;
int main()
{
    int num1;
    long long num2;

    cin >> num1 >> num2;
    cout << num1 << " + " << num2 << " = " << num1 + num2 << "\n";
    cout << num1 << " * " << num2 << " = " << num1 * num2 << "\n";
    cout << num1 << " - " << num2 << " = " << num1 - num2 << "\n";

}

